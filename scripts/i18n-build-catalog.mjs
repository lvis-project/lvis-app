#!/usr/bin/env node
/**
 * i18n catalog assembler.
 *
 * Globs the per-surface translation fragments written by the i18n migration
 * (`src/i18n/messages/generated/<namespace>.ts`, each exporting `en` and `ko`
 * objects) and regenerates:
 *   - `generated/index.ts` with the English default catalog only
 *   - `generated-locales/ko.ts` with the Korean language-pack catalog
 *
 * Idempotent and deterministic (fragments sorted by name) so re-running after
 * more fragments land just extends the barrel. Run after the migration:
 *   node scripts/i18n-build-catalog.mjs
 */
import { readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const MESSAGES_DIR = join(HERE, "..", "src", "i18n", "messages");
const GEN_DIR = join(MESSAGES_DIR, "generated");
const GENERATED_LOCALES_DIR = join(MESSAGES_DIR, "generated-locales");

function sanitize(name) {
  let id = name.replace(/[^A-Za-z0-9_]/g, "_");
  if (/^[0-9]/.test(id)) id = `_${id}`;
  return id;
}

const fragments = readdirSync(GEN_DIR)
  .filter((f) => f.endsWith(".ts") && f !== "index.ts")
  .map((f) => f.slice(0, -3))
  .sort();

const enImportLines = fragments
  .map((ns) => `import { en as en_${sanitize(ns)} } from "./${ns}.js";`)
  .join("\n");
const koImportLines = fragments
  .map((ns) => `import { ko as ko_${sanitize(ns)} } from "../generated/${ns}.js";`)
  .join("\n");

const enSpread = fragments.map((ns) => `  ...en_${sanitize(ns)},`).join("\n");
const koSpread = fragments.map((ns) => `  ...ko_${sanitize(ns)},`).join("\n");

const englishBody = `/**
 * AUTO-GENERATED barrel — do not edit by hand.
 *
 * Regenerated by \`scripts/i18n-build-catalog.mjs\`, which globs the per-surface
 * fragment files in this directory (\`<namespace>.ts\`, each exporting \`en\`/\`ko\`
 * objects) and merges the English default catalog here. Non-English generated
 * catalogs are language-pack chunks under \`../generated-locales/\`.
 *
 * Fragments: ${fragments.length}
 */
${enImportLines ? enImportLines + "\n" : ""}
export const generatedEn: Record<string, string> = {
${enSpread}
};
`;

const koreanBody = `/** AUTO-GENERATED by scripts/i18n-build-catalog.mjs. */
${koImportLines ? koImportLines + "\n" : ""}
export const koMessages: Record<string, string> = {
${koSpread}
};
`;

writeFileSync(join(GEN_DIR, "index.ts"), englishBody, "utf-8");
writeFileSync(join(GENERATED_LOCALES_DIR, "ko.ts"), koreanBody, "utf-8");
console.log(`[i18n] assembled ${fragments.length} fragment(s) into generated/index.ts + generated-locales/ko.ts`);
